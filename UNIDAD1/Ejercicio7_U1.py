num1 = int(input("Ingresa el primer numero (distinto de 0): "))

num2 = int(input("Ingresa el segundo numero (distinto de 0): "))

suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2

print(f"{num1} + {num2} da {suma}")
print(f"{num1} - {num2} da {resta}")
print(f"{num1} * {num2} da {multiplicacion}")
print(f"{num1} / {num2} da {division}")